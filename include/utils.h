int64_t xytoint(int64_t x, int64_t y) { return 1 << (60 - (COLSUMS[x] + y)); }

int64_t no_of_set_bits(int64_t x) {
	int64_t count = 0;
	while (x) {
		count += x & 1;
		x >>= 1;
	}
	return count;
}

